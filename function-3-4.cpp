#include <iostream>
using namespace std;

void print_pass_fail(char grade){

    switch (grade){
        case 'A':
            cout << "PASS" << endl;
            break;
        case 'B':
            cout << "PASS" << endl;
            break;
        case 'C':
            cout << "PASS" << endl;
            break;
        case 'D':
            cout << "FAIL" << endl;
            break;
        case 'E':
            cout << "FAIL" << endl;
            break;
    }

}